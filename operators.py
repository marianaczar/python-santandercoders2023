#numerical operators
num1 = 10
num2 = 20

print(num1 + num2)
print(num1 - num2)
print(num1 / num2)
print(num1 // num2)
print(20 % 3)
print(2 ** 3)

#boolean operators
age1 = 10
age2 = 15
height1 = 1.77
height2 = 1.65
height3 = height2

print(age1 > age2)            #false
print(age1 <= age2)           #true
print('Python' == 'python')   #false
print('banana' != 'pineaple') #true
print(height1 >= height2)     #true
print(height2 > height3)      #false